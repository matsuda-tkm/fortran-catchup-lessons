program array2d
    ! 2行3列の整数型配列 k を宣言
    integer :: k(2, 3)

    ! 1行目の要素を設定
    k(1, 1) = 11
    k(1, 2) = 12
    k(1, 3) = 13

    ! 2行目の全要素に、1行目の対応する要素の2倍を代入
    ! k(2,:) は k(2,1), k(2,2), k(2,3) を意味する
    k(2, :) = k(1, :) * 2

    ! 2行目の要素を個別に表示
    print *, k(2, 1), k(2, 2), k(2, 3)

    ! 配列全体の要素を表示 (注意: 出力順序)
    print *, k(:,:)

end program array2d
