program output

    print *, "Hello, World!"
    write(*,*) "This is a Fortran program."  ! print*と同じ意味

    ! print * と write(*,*) の違い
        ! print * は、標準出力に文字列を表示するための文法
        ! write(*,*) は、標準出力に文字列を表示するための文法
        ! ただし、write(*,*) は、フォーマットを指定することができる
        ! 例えば、write(*, '(A)') は、文字列を表示するための文法

end program output
